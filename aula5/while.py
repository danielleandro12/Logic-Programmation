##Exercício 1
#Escreva um programa que imprima os números de 1 a 10 usando um laço while.

##Exercício 2
#Peça ao usuário para digitar números. O programa deve somar esses números até que o usuário digite o número 0. Em seguida, exiba a soma total.

##Exercício 3
#Crie um programa que peça ao usuário para digitar a senha correta. Continue pedindo até que ele acerte a senha.

##Exercício 4
#Use um while para calcular o fatorial de um número dado pelo usuário. Certifique-se de que o número seja maior ou igual a zero.

##Exercício 5
#Crie um programa que exiba todos os números pares entre 1 e 50 usando um laço while.

##Exercício 6
#Escreva um programa que simule uma contagem regressiva de 10 até 1. No final, imprima "FIM!".

##Exercício 7
#Peça ao usuário para digitar um número. O programa deve continuar pedindo números e dizer se cada um é par ou ímpar. Pare o programa se o número digitado for negativo.

##Exercício 8
#Implemente um jogo de adivinhação onde o computador "pensa" em um número entre 1 e 100 (use um número fixo para teste). O usuário deve tentar adivinhar o número, e o programa deve dar dicas como "Maior" ou "Menor" até o usuário acertar.

##Exercício 9
#Faça um programa que leia um número inteiro e mostre a tabuada desse número de 1 a 10, usando um laço while.

##Exercício 10
#Crie um programa que simule um caixa eletrônico. O usuário deve digitar um valor a ser sacado (apenas valores inteiros). O programa deve calcular e exibir quantas notas de 100, 50, 20, 10 e 1 serão necessárias para realizar o saque. Use um laço while para processar o cálculo.

